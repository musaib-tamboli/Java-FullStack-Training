public class PositiveNegativeOrZero
{
    public static void main(String[] args) {
        int a = -10;

        if(a>0)
        {
            System.out.println(a+" is Positive");
        } else if (a<0) {
            System.out.println(a+" is Negative");
        }
        else
        {
            System.out.println(a+" is Zero");

        }
    }
}
